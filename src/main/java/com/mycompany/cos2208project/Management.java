    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextField2.getText().isEmpty() || (int) jSpinner1.getValue() < 0
                    || Double.parseDouble(jTextField1.getText()) < 0
                    || Double.parseDouble(jTextField3.getText()) < 0) {
                throw new Exception("Input invalide");
            }
            String title = jTextField2.getText();
            int qty = (int) jSpinner1.getValue();
            double price = Double.parseDouble(jTextField1.getText().trim());
            double discount = Double.parseDouble(jTextField3.getText().trim());
            ComboItem publisherItem = (ComboItem) publishersCombo.getSelectedItem();
            ComboItem authorItem = (ComboItem) authorsCombo.getSelectedItem();
            ComboItem categoryItem = (ComboItem) categoryCombo.getSelectedItem();
//            bookController.addBook(title, qty, price, discount, publisherItem.getKey(), authroItem.getKey());
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.addRow(new Object[]{title, qty, price, discount, publisherItem, authorItem, categoryItem});

            jTextField2.setText("");
            jSpinner1.setValue(0);
            jTextField1.setText("");
            jTextField3.setText("");
            publishersCombo.setSelectedIndex(0);
            authorsCombo.setSelectedIndex(0);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Price,Discount must be Number", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

