    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        TableModel model = jTable2.getModel();
        int rowCount = model.getRowCount();
        ArrayList<Book> books = new ArrayList<>();

        for (int row = 0; row < rowCount; row++) {
            String title = model.getValueAt(row, 0).toString();
            int qty = Integer.parseInt(model.getValueAt(row, 1).toString());
            double price = Double.parseDouble(model.getValueAt(row, 2).toString());
            double discount = Double.parseDouble(model.getValueAt(row, 3).toString());
            ComboItem publisherItem = (ComboItem) model.getValueAt(row, 4);
            ComboItem authorItem = (ComboItem) model.getValueAt(row, 5);
            ComboItem categoryItem = (ComboItem) model.getValueAt(row, 6);
            Book book = new Book(title, qty, price, discount, publisherItem.getKey(), authorItem.getKey(), categoryItem.getKey());
            books.add(book);
        }

        bookController.addBooks(books);
    }//GEN-LAST:event_jButton17ActionPerformed

