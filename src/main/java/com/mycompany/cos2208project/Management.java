    private void firstAction() {
        listLabel.setText("Book List");
        ArrayList<Book> books = bookController.getBooks();
        String[] columns = {"ID", "Title", "qty", "Price", "Discount", "Publisher ID", "Author ID", "Category ID"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);

        for (Book book : books) {
            Object[] row = {
                book.getId(),
                book.getTitle(),
                book.getQty(),
                book.getPrice(),
                book.getDiscount(),
                book.getPublisherId(),
                book.getAuthorId(),
                book.getCategoryId()
            };
            model.addRow(row);
        }
        jTable1.setModel(model);

        for (MouseListener listener : jTable1.getMouseListeners()) {
            jTable1.removeMouseListener(listener);
        }

        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = jTable1.rowAtPoint(evt.getPoint());
                int col = jTable1.columnAtPoint(evt.getPoint());

                if (col == 0) {
                    Object bookId = jTable1.getValueAt(row, col);
                    new BookDetail((int) bookId);
                    dispose();
                }

                if (col == 5) {
                    Object publisherId = jTable1.getValueAt(row, col);
                    new PublisherDetail((int) publisherId);
                    dispose();
                }

                if (col == 6) {
                    //author
                    Object authorId = jTable1.getValueAt(row, col);
                    new AuthorDetail((int) authorId);
                    dispose();

                }

                if (col == 7) {
                    Object categoryId = jTable1.getValueAt(row, col);
                    new CategoryDetail((int) categoryId);
                    dispose();
                }
            }
        });
    }


